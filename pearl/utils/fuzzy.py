import re
from typing import Collection, Any, Callable, Union

def finder(text: Any, collection: Collection, *,
           key: Callable=None, lazy: bool=True) -> Union[list, tuple]:
    '''
    Finds a text from a collection.
    '''
    suggestions = []
    text = str(text)

    pattern = '.*?'.join(map(re.escape, text))
    regex = re.compile(pattern, flags=re.IGNORECASE)

    for item in collection:
        to_search = key(item) if key else item
        match = regex.search(to_search)
        
        if match:
            suggestions.append((len(match.group()), match.start(), item))

    def sort_key(tup: tuple):
        if key:
            return tup[0], tup[1], key(tup[2])
        return tup

    if lazy:
        return (z for _, _, z in sorted(suggestions, key=sort_key))
    else:
        return [z for _, _, z in sorted(suggestions, key=sort_key)]